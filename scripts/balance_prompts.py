"""Balance prompts: keep faithfulness constraints while recovering answer relevancy."""
import re, sys

def replace_in_file(path, pairs):
    with open(path, "r", encoding="utf-8") as f:
        content = f.read()
    for old, new in pairs:
        if old not in content:
            print(f"  [WARN] pattern not found in {path}:\n    {old[:80]}...")
            continue
        count = content.count(old)
        if count > 1:
            print(f"  [WARN] pattern found {count} times in {path}, replacing all:\n    {old[:80]}...")
        content = content.replace(old, new)
        print(f"  [OK] replaced in {path}")
    with open(path, "w", encoding="utf-8") as f:
        f.write(content)

